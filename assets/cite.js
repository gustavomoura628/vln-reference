/* cite.js — footnote citations.
   Any <a class="cite" data-key="..."></a> (in prose or generated by the matrix) becomes a
   numbered superscript. Numbers are assigned per page in order of first appearance; a reused
   source keeps its number. Hover / focus / tap shows a plain tooltip: source, locus, a verbatim
   snippet, and a link to the source. A #references list, if present, is filled in number order.
   The global toggle hides all markers for clutter-free reading. */
(function () {
  const TYPE = { P:"paper", H:"our use", R:"code", HF:"model card", README:"repo", web:"web" };
  const CITES = window.CITES || {};
  let tip, hideTimer = null, pinned = null;

  function ensureTip() {
    if (tip) return;
    tip = document.createElement("div");
    tip.className = "tip";
    document.body.appendChild(tip);
    tip.addEventListener("mouseenter", () => clearTimeout(hideTimer));
    tip.addEventListener("mouseleave", scheduleHide);
  }

  function fill(key, num) {
    const c = CITES[key];
    if (!c) return false;
    const snip = c.snippet ? `<q class="tip-q">${c.snippet}</q>` : "";
    tip.innerHTML =
      `<div class="tip-h"><span class="tip-n">${num}</span>` +
      `<span class="tip-type">${TYPE[c.type] || c.type}</span>` +
      `<span class="tip-loc">${c.label}</span></div>` +
      snip +
      `<a class="tip-link" href="${c.url}" target="_blank" rel="noopener">view source · ${c.locus} ↗</a>`;
    return true;
  }

  function place(marker) {
    const m = marker.getBoundingClientRect();
    tip.style.visibility = "hidden"; tip.classList.add("show");
    const tw = tip.offsetWidth, th = tip.offsetHeight, pad = 12, vw = innerWidth;
    let left = Math.max(pad, Math.min(m.left + m.width / 2 - tw / 2, vw - tw - pad)) + scrollX;
    let top = (innerHeight - m.bottom > th + 14 || m.top < th + 14)
      ? m.bottom + 8 + scrollY : m.top - th - 8 + scrollY;
    tip.style.left = left + "px"; tip.style.top = top + "px"; tip.style.visibility = "visible";
  }

  function show(marker) {
    if (document.body.classList.contains("cites-off")) return;
    clearTimeout(hideTimer); ensureTip();
    if (fill(marker.dataset.key, marker.textContent)) place(marker);
  }
  function scheduleHide() { hideTimer = setTimeout(() => { if (!pinned) tip && tip.classList.remove("show"); }, 130); }
  function hardHide() { pinned = null; tip && tip.classList.remove("show"); }

  function wire(marker) {
    if (marker.dataset.hy) return;
    marker.dataset.hy = "1";
    marker.setAttribute("tabindex", "0");
    const c = CITES[marker.dataset.key];
    marker.setAttribute("aria-label", c ? "Source: " + c.label : "Source");
    marker.addEventListener("mouseenter", () => show(marker));
    marker.addEventListener("mouseleave", scheduleHide);
    marker.addEventListener("focus", () => show(marker));
    marker.addEventListener("blur", scheduleHide);
    marker.addEventListener("click", (e) => {
      e.preventDefault();
      if (pinned === marker) hardHide();
      else { pinned = marker; show(marker); }
    });
  }

  // assign footnote numbers across the whole document, in reading order
  function renumber() {
    const order = new Map();
    let n = 0;
    document.querySelectorAll("a.cite[data-key]").forEach((m) => {
      const k = m.dataset.key;
      if (!order.has(k)) order.set(k, ++n);
      m.textContent = order.get(k);
      wire(m);
    });
    return order;
  }

  function buildReferences(order) {
    const box = document.getElementById("references");
    if (!box) return;
    const items = [...order.entries()].sort((a, b) => a[1] - b[1]);
    box.innerHTML = items.map(([k, num]) => {
      const c = CITES[k]; if (!c) return "";
      const tail = c.locus && !c.label.includes(c.locus) ? ` — ${c.locus}` : "";
      return `<li><span class="ref-n">${num}</span>` +
        `<span class="ref-type">${TYPE[c.type] || c.type}</span> ` +
        `<a href="${c.url}" target="_blank" rel="noopener">${c.label}${tail} ↗</a></li>`;
    }).join("");
  }

  function hydrate() { buildReferences(renumber()); }

  function marker(key) {
    const a = document.createElement("a");
    a.className = "cite"; a.dataset.key = key;
    a.href = (CITES[key] && CITES[key].url) || "#";
    return a;
  }
  function markers(keys) {
    const f = document.createDocumentFragment();
    (keys || []).forEach((k) => f.appendChild(marker(k)));
    return f;
  }

  document.addEventListener("click", (e) => { if (pinned && !tip.contains(e.target) && e.target !== pinned) hardHide(); });
  document.addEventListener("keydown", (e) => { if (e.key === "Escape") hardHide(); });
  addEventListener("scroll", () => { if (!pinned) tip && tip.classList.remove("show"); }, { passive: true });

  window.Cite = { hydrate, marker, markers };
  document.addEventListener("DOMContentLoaded", hydrate);
})();
